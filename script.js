// ? Funcion de busqueda y formato de datos
function busqueda() {
  if (cargando == false) {
    cargando = true;
    var palabra = $("#S3search").val().toLowerCase();
    var conten = $("#searchresults");
    var dato = "";
    var datos = "";
    conten.html(loader);

    if (palabra == "") {
      conten.html("");
      $("#formulariofinal").fadeOut();
      $("#btnfinal").fadeOut();
      document.getElementById("formfinaldata").reset();
      cargando = false;
      return;
    }

    datosBusqueda.forEach((element) => {
      var verif = element["pais"].toLowerCase().indexOf(palabra);
      if (verif !== -1) {
        dato = searchresult;
        dato = dato.replaceAll("{PAIS}", element["pais"]);
        dato = dato.replaceAll("{CODIGO}", element["clave"]);
        datos += dato;
      }
    });

    conten.html(datos);
    cargando = false;
  }
}
